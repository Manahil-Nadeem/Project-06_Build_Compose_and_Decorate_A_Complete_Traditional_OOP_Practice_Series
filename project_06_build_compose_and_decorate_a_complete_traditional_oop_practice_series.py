# 18. Property Decorators: @property, @setter, and @deleter:

class Product:
    def __init__(self, price):
        self._price = price  # Private attribute

    # Getter method using @property
    @property
    def price(self):
        print("ðŸ“¦ Getting the price...")
        return self._price

    # Setter method using @price.setter
    @price.setter
    def price(self, value):
        if value < 0:
            print("âš ï¸ Price cannot be negative!")
        else:
            print("ðŸ”§ Setting the price...")
            self._price = value

    # Deleter method using @price.deleter
    @price.deleter
    def price(self):
        print("âŒ Deleting the price...")
        del self._price
